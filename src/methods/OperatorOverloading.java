package methods;

public class OperatorOverloading {

	public static void main(String[] args) {
		System.out.println("Operator Overloading with Plus Operator\n");
		System.out.println(10 + 10);
		System.out.println(10.5 + 10.5);
		System.out.println(20 + 10.5);
		System.out.println('A' + 10.5);
		System.out.println("Aimerz " + 10.5 + 10);
		System.out.println("Aimerz" + ".ai");
	}

}
