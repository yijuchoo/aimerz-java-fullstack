package operators;

public class ArithmeticOperators {
	
	public static void main(String[] args) {
		System.out.println("Write in Print Statement");
		System.out.println("5 + 1 = " + (5 + 1));
		
		// Declare & initialize variable
		System.out.println("\n\nUsing Variables with Arithmetic Operations");
		int a = 20;
		int b = 10;
		System.out.println("a = " + a + ", b = " + b);
		System.out.println("The sum of a + b = " + (a + b));
		System.out.println("The difference of a - b = " + (a - b));
		System.out.println("The production of a * b = " + (a * b));
		System.out.println("The division of a / b = " + (a / b));
		
	}
}
