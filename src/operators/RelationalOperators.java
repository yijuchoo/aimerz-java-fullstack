package operators;

public class RelationalOperators {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;

		System.out.println("===== Relational Operators =====");

		System.out.println("Check a == (is equals to) b - " + (a == b));
		System.out.println("Check a != (not equals to) b - " + (a != b));
		System.out.println("Check a > (greater than) b - " + (a > b));
		System.out.println("Check a < (less than) b - " + (a < b));
		System.out.println(
				"Check a >= (greater than or equals to) b - " + (a >= b));
		System.out
				.println("Check a <= (less than or equals to) b - " + (a <= b));

	}
}
