package beans;

public class Engine {

	private int engineCapacity;

	// Constructor
	public Engine(int engineCapacity) {
		super();
		this.engineCapacity = engineCapacity;
	}
	
	// Getters and Setters
	public int getEngineCapacity() {
		return engineCapacity;
	}

	public void setEngineCapacity(int engineCapacity) {
		this.engineCapacity = engineCapacity;
	}


}
